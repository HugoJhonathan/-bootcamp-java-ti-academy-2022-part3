package repeticao;

public class OpCompostos {

	public static void main(String[] args) {
		int x = 3;
		int y = 5;

		System.out.println(x += 7); // 10
		System.out.println(x -= 4); // 6
		System.out.println(y *= x); // 30
		System.out.println(y /= 10);// 3
		System.out.println(y %= 3); // 0

	}

}
